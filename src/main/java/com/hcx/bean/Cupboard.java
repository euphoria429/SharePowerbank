package com.hcx.bean;

public class Cupboard {
    private Integer cupboardId;

    private Integer locationId;

    private Integer pobkNum;

    private Integer pobkAvailableNum;

    public Integer getCupboardId() {
        return cupboardId;
    }

    public void setCupboardId(Integer cupboardId) {
        this.cupboardId = cupboardId;
    }

    public Integer getLocationId() {
        return locationId;
    }

    public void setLocationId(Integer locationId) {
        this.locationId = locationId;
    }

    public Integer getPobkNum() {
        return pobkNum;
    }

    public void setPobkNum(Integer pobkNum) {
        this.pobkNum = pobkNum;
    }

    public Integer getPobkAvailableNum() {
        return pobkAvailableNum;
    }

    public void setPobkAvailableNum(Integer pobkAvailableNum) {
        this.pobkAvailableNum = pobkAvailableNum;
    }
}